package Module1.Lecture2;

public class Hello {
    // Method Signature
    // Access Modifier: public (other classes can see and use this method)
    // Other Modifier(s): static (this method belongs to the class itself)
    // Return Type: void (does not return a value)
    // Name: main
    // Parameter(s): String[] args (A single string array, named args)
    public static void main(String[] args) {
        System.out.println("Hello");
    }
}
